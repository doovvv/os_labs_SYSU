extern "C" void setup_kernel()
{

    // 中断管理器
    interruptManager.initialize();
    interruptManager.enableTimeInterrupt();
    interruptManager.setTimeInterrupt((void *)asm_time_interrupt_handler);

    // 输出管理器
    stdio.initialize();

    // 进程/线程管理器
    programManager.initialize();

    // 内存管理器
    int index = 512;
    int offset = 290;
    int virtual_address = index*PAGE_SIZE+offset;
    int physical_address = 1000*PAGE_SIZE+offset;
    printf("physical address:%x\n",physical_address);
    *(int *)physical_address = 22336290;
    memoryManager.openPageMechanism();
    memoryManager.initialize();
    printf("virtual address:%x\n",virtual_address);
    printf("%d",*(int *)virtual_address);

    // 创建第一个线程
    int pid = programManager.executeThread(first_thread, nullptr, "first thread", 1);
    if (pid == -1)
    {
        printf("can not execute thread\n");
        asm_halt();
    }
    ListItem *item = programManager.readyPrograms.front();
    PCB *firstThread = ListItem2PCB(item, tagInGeneralList);
    firstThread->status = RUNNING;
    programManager.readyPrograms.pop_front();
    programManager.running = firstThread;
    asm_switch_thread(0, firstThread);

    asm_halt();
}
